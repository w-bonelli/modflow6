def get_local_data(idx):
    ncolst = ncols[idx]
    nmodels = len(ncolst)
    mnames = []
    for jdx in range(nmodels):
        mname = f"gwf{jdx + 1}"
        mnames.append(mname)
    return ncolst, nmodels, mnames


def build_model(idx, dir):
    name = ex[idx]

    # set local data for this model
    ncolst, nmodels, mnames = get_local_data(idx)

    c6left = []
    c6right = []
    vl = hbndl[0]
    vr = hbndl[1]
    for k in range(nlay):
        for i in range(nrow):
            c6left.append([(k, i, 0), vl, "left"])
            c6right.append([(k, i, ncols[idx][-1] - 1), vr, "right"])
    cd6left = {0: c6left}
    cd6right = {0: c6right}

    # build MODFLOW 6 files
    ws = dir
    sim = flopy.mf6.MFSimulation(
        sim_name=name,
        memory_print_option="all",
        version="mf6",
        exe_name="mf6",
        sim_ws=ws,
    )
    # create tdis package
    tdis = flopy.mf6.ModflowTdis(
        sim, time_units="DAYS", nper=nper, perioddata=tdis_rc
    )

    # set ims csv files
    csv0 = f"{name}.outer.ims.csv"
    csv1 = f"{name}.inner.ims.csv"

    # create iterative model solution and register the gwf model with it
    ims = flopy.mf6.ModflowIms(
        sim,
        print_option="ALL",
        csv_outer_output_filerecord=csv0,
        csv_inner_output_filerecord=csv1,
        outer_dvclose=hclose * 10.0,
        outer_maximum=nouter,
        under_relaxation="NONE",
        inner_maximum=ninner,
        inner_dvclose=hclose,
        rcloserecord=rclose,
        linear_acceleration="CG",
        scaling_method="NONE",
        reordering_method="NONE",
        relaxation_factor=relax,
    )
    sim.register_ims_package(ims, mnames)

    # create mover and exchange file, if needed
    if nmodels > 1:
        # mover file and package name
        fnmvr = f"{name}.mvr"
        pmvr = "simmvr"

        # exchange data
        exchd = []
        for k in range(nlay):
            for i in range(nrow):
                t = []
                t.append((k, i, ncolst[0] - 1))
                t.append((k, i, 0))
                t.append(1)
                t.append(delr / 2.0)
                t.append(delr / 2.0)
                t.append(delc)
                exchd.append(t)
        excf = flopy.mf6.ModflowGwfgwf(
            sim,
            exgtype="GWF6-GWF6",
            nexg=len(exchd),
            exgmnamea=mnames[0],
            exgmnameb=mnames[1],
            exchangedata=exchd,
        )

        # mover data
        mvrpd = []
        t = []
        packages = []
        for jdx in range(nmodels):
            mname = mnames[jdx]
            pname = f"sfr{jdx + 1}"
            packages.append([mname, pname])
            id = 0
            if jdx == 0:
                id = ncolst[jdx] - 1
            t.append(mname)
            t.append(pname)
            t.append(id)
        t.append("FACTOR")
        t.append(1.0)
        t = tuple(t)
        mvrpd.append(t)
        # create entry for maw
        packages.append([mnames[1], "maw"])
        mvrpd.append(
            (mnames[1], "maw", (0), mnames[0], "sfr1", (sfrdst), "FACTOR", 1.0)
        )
        # create mvr package
        mmvr = flopy.mf6.ModflowMvr(
            excf,
            print_input=True,
            modelnames=True,
            maxmvr=len(mvrpd),
            maxpackages=len(packages),
            packages=packages,
            perioddata={0: mvrpd},
            filename=fnmvr,
            pname=pmvr,
        )

    # create gwf model
    for jdx in range(nmodels):
        mname = mnames[jdx]
        i0 = 0
        if jdx > 0:
            i0 += ncolst[jdx - 1]
        i1 = i0 + ncolst[jdx]
        hkt = hk[:, i0:i1]

        gwf = flopy.mf6.ModflowGwf(
            sim, modelname=mname, model_nam_file=f"{mname}.nam"
        )

        dis = flopy.mf6.ModflowGwfdis(
            gwf,
            nlay=nlay,
            nrow=nrow,
            ncol=ncolst[jdx],
            delr=delr,
            delc=delc,
            top=top,
            botm=bot,
            filename=f"{mname}.dis",
        )

        # initial conditions
        ic = flopy.mf6.ModflowGwfic(gwf, strt=strt, filename=f"{mname}.ic")

        # node property flow
        npf = flopy.mf6.ModflowGwfnpf(
            gwf, save_flows=False, icelltype=0, k=hkt
        )

        # chd files
        if jdx == 0:
            fn = f"{mname}.chd1.chd"
            chd1 = flopy.mf6.modflow.ModflowGwfchd(
                gwf,
                boundnames=True,
                stress_period_data=cd6left,
                save_flows=False,
                filename=fn,
                pname="chd1",
                print_input=True,
            )
        if jdx == nmodels - 1:
            fn = f"{mname}.chd2.chd"
            chd2 = flopy.mf6.modflow.ModflowGwfchd(
                gwf,
                boundnames=True,
                stress_period_data=cd6right,
                save_flows=False,
                filename=fn,
                pname="chd2",
                print_input=True,
            )

        # sfr models
        rchd = []
        connd = []
        ipos = 0
        if jdx > 0:
            ipos += ncolst[jdx - 1]
        for n in range(ncolst[jdx]):
            connt = [n]
            if n > 0:
                connt.append(n - 1)
            if n < ncolst[jdx] - 1:
                connt.append(-(n + 1))
            connd.append(connt)
            nconn = len(connt) - 1  # remove sfrno item from count
            zbed = 14.0 - (float(ipos) + 0.5) * delr * slope
            rcht = [
                n,
                (0, 25, n),
                delr,
                width,
                slope,
                zbed,
                bthick,
                sfrhk,
                rough,
                nconn,
                ustrf,
                ndiv,
            ]
            rchd.append(rcht)
            ipos += 1

        perioddata = None
        if jdx == 0:
            perioddata = [[0, "inflow", 5.0]]

        pname = f"sfr{jdx + 1}"
        fn = f"{mname}.{pname}.sfr"
        cnvgpth = f"{mname}.sfr.cnvg.csv"

        sfr = flopy.mf6.ModflowGwfsfr(
            gwf,
            unit_conversion=unit_conv,
            print_stage=True,
            print_flows=True,
            package_convergence_filerecord=cnvgpth,
            mover=True,
            nreaches=len(rchd),
            packagedata=rchd,
            connectiondata=connd,
            perioddata=perioddata,
            filename=fn,
            pname=pname,
            print_input=True,
        )

        # maw files
        if jdx == nmodels - 1:
            mpd = [[0, 0.25, bot, strt, "THIEM", 1, "MYWELL"]]
            mcd = [
                [0, 0, (0, 15, int(ncolst[jdx]) - 31), top, bot, 999.0, 999.0]
            ]
            perioddata = [[0, "RATE", -1e-5]]
            maw = flopy.mf6.ModflowGwfmaw(
                gwf,
                boundnames=True,
                print_input=True,
                print_head=True,
                print_flows=True,
                nmawwells=len(mpd),
                packagedata=mpd,
                connectiondata=mcd,
                perioddata=perioddata,
                pname="maw",
                mover=True,
            )
            if nmodels == 1:
                packages = [(pname,), ("maw",)]
                mpd = [("maw", 0, pname, sfrdst, "FACTOR", 1.0)]
                fn = f"{mname}.mvr"
                # create mvr package
                mvr = flopy.mf6.ModflowGwfmvr(
                    gwf,
                    print_input=True,
                    maxmvr=len(mpd),
                    maxpackages=len(packages),
                    packages=packages,
                    perioddata={0: mpd},
                    filename=fn,
                )

        # output control
        oc = flopy.mf6.ModflowGwfoc(
            gwf,
            budget_filerecord=f"{mname}.cbc",
            head_filerecord=f"{mname}.hds",
            headprintrecord=[
                ("COLUMNS", 10, "WIDTH", 15, "DIGITS", 6, "GENERAL")
            ],
            saverecord=[("HEAD", "LAST")],
            printrecord=[("HEAD", "LAST"), ("BUDGET", "LAST")],
        )

    return sim, None


